mylist =[21,5,7,2,3,9,100,1,56]
print(mylist)

print("sorted list")
mylist.sort()
print(mylist)

print("reverse list")
mylist.reverse()
print(mylist)

print("append list")
mylist.append(8)
print(mylist)

print("insert List")
mylist.insert(2,10)
print(mylist)

print("pop method")
mylist.pop(1)
print(mylist)